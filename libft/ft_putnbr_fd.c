#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int nombre;

	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n == 0)
		ft_putchar_fd('0', fd);
	if (n >= 1)
	{
		nombre = n % 10 + '0';
		if ((n / 10) != 0)
			ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd(nombre, fd);
	}
}
