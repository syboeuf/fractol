#include "libft.h"

void	ft_putnbr(int n)
{
	int nombre;

	if (n == -2147483648)
		ft_putstr("-2147483648");
	else if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
	}
	if (n == 0)
		ft_putchar('0');
	if (n >= 1)
	{
		nombre = n % 10 + '0';
		if ((n / 10) != 0)
			ft_putnbr(n / 10);
		ft_putchar(nombre);
	}
}
